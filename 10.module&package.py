# ============================================
# 📦 모듈(module)과 패키지(package) 정리
# ============================================

# 1. 모듈 (module)
# - 함수, 변수, 클래스 등을 모아놓은 파이썬 파일(.py)
# - 코드의 재사용성과 관리 용이성을 위해 사용

# 🔹 모듈 불러오기(Import)
# import module_이름
#    -> 모듈 전체를 불러옴
# from module_이름 import 함수_이름
#    -> 모듈에서 특정 함수(또는 변수, 클래스)만 선택적으로 불러옴

# 2. 패키지 (package)
# - 여러 개의 모듈을 하나의 디렉토리(폴더)에 모아 놓은 것
# - 코드의 재사용성과 구조화를 도와줌
# - 다른 사람이 만든 코드도 패키지로 제공됨

# 🔹 패키지 설치/관리 명령어
# pip install 패키지이름         # 패키지 설치
# pip list                      # 설치된 패키지 목록 확인
# pip install --upgrade 패키지이름 # 패키지 업그레이드
# pip uninstall 패키지이름       # 패키지 삭제

# ============================================
# 요약:
#    - 모듈: 파이썬 파일(.py) 하나
#    - 패키지: 여러 모듈을 폴더에 모아 놓은 구조
# ============================================

# 패키지를 이용하면 코드를 효율적으로 관리하고,
# 다른 사람이 만든 다양한 기능을 쉽게 활용할 수 있습니다.

