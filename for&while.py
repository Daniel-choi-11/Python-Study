# 반복문 
# 반복문은 코드를 여러번 사용하고싶을때 사용된다.

# 1. for문 
# 문자열, 리스트, 튜플, 등과 같은 항목의 순서대로 접근할때 사용됩니다.
food = ["김치", "사과", "배"] 
for food in food:
    print(food)

# 1-1. range() 함수
# for문에서 반복함수를 지정하고싶을때는 range함수를 사용할수있습니다
# i는 가상의 변수다 (허수)
for i in range(5):
    print("안녕하세요")

